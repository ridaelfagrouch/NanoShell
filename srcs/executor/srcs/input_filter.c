t_node	*get_instruction(t_node *cmds_head)
{
	static t_node	*head = cmds_head;
	t_node			*node;
	t_node			*tracer;

	tracer = head;
	if (tracer->token == APPEND || tracer->token == IN)
	{
		node = tracer;
		while (tracer && (tracer->token == APPEND || tracer->token == IN))
			tracer = tracer->next;
		head = tracer;
	}
}

