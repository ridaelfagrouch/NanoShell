int	export_var_to_env(t_env *head, char *key_value)
{
	t_env	**tracer;
	t_env	*node;

	tracer = &head;
	while ((*tracer)->next)
		tracer = &(*tracer)->next;
	node = (t_env *)ft_calloc(1, sizeof(t_env));
	if (node == NULL || fill_node_with_key_value_env(key_value, node) != 0)
		return (free(node), -1);
	(*tracer)->next = node;
	return (0);
}

